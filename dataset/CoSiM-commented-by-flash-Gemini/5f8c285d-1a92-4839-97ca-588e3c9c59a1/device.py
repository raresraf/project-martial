class Device(object):
    """
    Represents a single device within a simulated distributed environment.
    Each device manages its own sensor data, communicates with a supervisor,
    and orchestrates multi-threaded script execution through its `DeviceThread`.
    It uses a lock to protect its `sensor_data` and an event to signal
    script assignment completion.
    """

    def __init__(self, device_id, sensor_data, supervisor):
        """
        Initializes a Device instance.

        Args:
            device_id (int): A unique identifier for the device.
            sensor_data (dict): A dictionary containing initial sensor data for the device.
            supervisor (object): A reference to a supervisor object for inter-device communication.
        """
        
        self.device_id = device_id
        self.sensor_data = sensor_data
        self.supervisor = supervisor
        self.scripts = [] # List to hold assigned scripts (tuples of (script, location)).
        self.lock = Lock() # A lock to protect this device's `sensor_data` from concurrent access.
        self.all_scripts_received = Event() # Event to signal when all scripts for a timepoint have been assigned.
        self.barrier = None # Placeholder for the global ReusableBarrier, set in setup_devices.
        self.thread = None # Placeholder for the DeviceThread, initialized in setup_devices.
        self.devices = None # Placeholder for a list of all devices in the simulation, used by the master device.

    def __str__(self):
        """
        Returns a string representation of the device.

        Returns:
            str: A string in the format "Device <device_id>".
        """
        
        return "Device %d" % self.device_id

    def setup_devices(self, devices):
        """
        Initializes a global barrier and spawns `DeviceThread` instances for all devices.
        This method is designed to be called only by the device with `device_id == 0`.
        It creates a single `ReusableBarrier` and assigns each device its own `DeviceThread`.

        Args:
            devices (list): A list of all `Device` objects in the simulation.
        """
        # Block Logic: Only the device with device_id 0 performs this setup.
        if self.device_id is 0:
            self.devices = devices # Store references to all devices.
            
            self.barrier = ReusableBarrier(len(devices)) # Inline: Creates a global ReusableBarrier.
            self.thread = DeviceThread(self, self.lock, self.barrier) # Create the DeviceThread for this master device.
            for dev in devices:
                if dev.device_id is not 0: # For all other devices.
                    dev.barrier = self.barrier # Assign the global barrier.
                    # Inline: Create the DeviceThread for the current device and assign its private lock and the global barrier.
                    dev.thread = DeviceThread(dev, dev.lock, self.barrier)
                dev.thread.start() # Start each DeviceThread.

    def assign_script(self, script, location):
        """
        Assigns a script to the device or signals that all scripts have been received for a timepoint.
        If a script is provided, it's appended to the device's internal script list.
        If `script` is None, it signals that script assignments for the current
        timepoint are complete by setting `all_scripts_received`.

        Args:
            script (object): The script object to be executed, or None to signal end of assignments.
            location (int): The location identifier in the sensor data to which the script applies.
        """
        if script is not None:
            
            self.scripts.append((script, location)) # Add the script and its location to the list.
        else:
            
            self.all_scripts_received.set() # If script is None, signal that script assignments are done for the timepoint.

    def get_data(self, location):
        """
        Retrieves sensor data for a given location from this device's `sensor_data` dictionary.

        Args:
            location (int): The location identifier for which to retrieve data.

        Returns:
            any: The sensor data at the specified location, or None if the location is not found.
        """
        if location in self.sensor_data:
            return self.sensor_data[location]
        else:
            return None


    def set_data(self, location, data):
        """
        Sets sensor data for a given location in this device's `sensor_data` dictionary.
        The data is updated only if the location exists in the `sensor_data`.

        Args:
            location (int): The location identifier for which to set data.
            data (any): The new data value to be set.
        """
        if location in self.sensor_data:
            self.sensor_data[location] = data

    def shutdown(self):
        """
        Initiates the shutdown process for the entire distributed system.
        This method is designed to be called only by the device with `device_id == 0`.
        It waits for all `DeviceThread` instances across all devices to complete their execution.
        """
        
        # Block Logic: Only the device with device_id 0 is responsible for initiating the shutdown of all threads.
        if self.device_id is 0:
            for dev in self.devices: # Iterate through all devices in the simulation.
                dev.thread.join() # Wait for each device's DeviceThread to finish its execution.




class DeviceThread(Thread):
    """
    The main thread for a `Device`. It acts as an orchestrator, responsible for
    fetching neighbor information from the supervisor, waiting for script assignments,
    executing all assigned scripts for a timepoint, and participating in global
    synchronization via a shared barrier.
    """

    def __init__(self, device, lock, barrier):
        """
        Initializes a `DeviceThread` instance.

        Args:
            device (Device): The parent `Device` object this thread belongs to.
            lock (Lock): The `Lock` associated with the parent `Device` for its `sensor_data`.
            barrier (ReusableBarrier): The global `ReusableBarrier` for inter-device synchronization.
        """
        Thread.__init__(self, name="Device Thread %d" % device.device_id)
        self.device = device
        self.lock = lock
        self.barrier = barrier

    def run(self):
        """
        The main execution loop for the `DeviceThread`.
        It continuously fetches neighbor data, waits for script assignments,
        executes all assigned scripts for a timepoint, and participates in
        global synchronization using the shared barrier.
        """

        while True:
            # Block Logic: Fetch neighbor information from the supervisor.
            neighbours = self.device.supervisor.get_neighbours()

            # Inline: If `neighbours` is None, it signals termination for the device.
            if neighbours is None:
                break # Exit the main loop, terminating the DeviceThread.
            
            # Block Logic: Wait for the `all_scripts_received` event to be set,
            # indicating that all scripts for the current timepoint have been assigned.
            self.device.all_scripts_received.wait()
            
            # Inline: Clear the event for the next timepoint.
            self.device.all_scripts_received.clear()

            # Block Logic: Iterate through all assigned scripts for the current timepoint,
            # collecting data, executing the script, and updating device states.
            for (script, location) in self.device.scripts:
                script_data = [] # List to collect input data for the current script.
                
                # Block Logic: Collect data from all neighboring devices at the specified location.
                for device in neighbours:
                    # Inline: Acquire the neighbor's private lock before accessing its sensor data.
                    device.lock.acquire()
                    data = device.get_data(location) # Get data from the neighbor.
                    if data is not None:
                        script_data.append(data) # Add to script input if available.
                
                # Block Logic: Collect data from this device's own sensor data.
                data = self.device.get_data(location)
                if data is not None:
                    script_data.append(data) # Add to script input if available.

                # Block Logic: If input data is available, execute the script and update device data.
                if script_data != []:
                    # Inline: Execute the script's `run` method with the collected data.
                    result = script.run(script_data)

                    # Block Logic: Update sensor data for all involved devices (neighbors and self) with the result.
                    for device in neighbours:
                        device.set_data(location, result) # Update neighbor's data.

                    self.device.set_data(location, result) # Update this device's own data.
                
                # Block Logic: Release the locks acquired from neighboring devices.
                for device in neighbours:
                    device.lock.release() # Release neighbor's private lock.

            # Block Logic: Synchronize with other devices at the global barrier.
            # This ensures all devices have completed their script processing for the timepoint.
            self.barrier.wait()

class ReusableBarrier():
    """
    A reusable barrier synchronization mechanism for multiple threads using semaphores.
    This barrier allows a fixed number of threads to wait at a synchronization point,
    and once all threads arrive, they are all released simultaneously. The counters
    are stored in lists (`count_threads1`, `count_threads2`) to allow for reusability
    of the barrier instance across multiple `wait` calls.
    """
    
    def __init__(self, num_threads):
        """
        Initializes the reusable barrier with a specified number of threads.

        Args:
            num_threads (int): The total number of threads that will participate
                               in the barrier synchronization.
        """
        self.num_threads = num_threads
        self.count_threads1 = [self.num_threads] # Counter for the first phase of the barrier.
        self.count_threads2 = [self.num_threads] # Counter for the second phase of the barrier.
        self.count_lock = Lock() # Lock to protect the shared counters during decrements and resets.
        self.threads_sem1 = Semaphore(0) # Semaphore for the first phase of threads to wait on.
        self.threads_sem2 = Semaphore(0) # Semaphore for the second phase of threads to wait on.

    def wait(self):
        """
        Causes the calling thread to wait until all threads have reached this barrier.
        This method orchestrates a two-phase synchronization to ensure reusability
        without deadlocks.
        """
        
        self.phase(self.count_threads1, self.threads_sem1)
        self.phase(self.count_threads2, self.threads_sem2)

    def phase(self, count_threads, threads_sem):
        """
        Executes a single phase of the barrier synchronization.

        Args:
            count_threads (list): A list containing the current count of threads
                                  remaining for this phase.
            threads_sem (Semaphore): The semaphore threads wait on for this phase.
        """
        
        with self.count_lock: # Protect shared counter access.
            count_threads[0] -= 1 # Decrement the count of threads remaining.
            
            if count_threads[0] == 0: # If this is the last thread to arrive:
                for i in range(self.num_threads):
                    threads_sem.release() # Release all waiting threads by incrementing the semaphore.
                count_threads[0] = self.num_threads # Reset counter for next use.
        
        threads_sem.acquire() # Wait (decrement) the semaphore, blocking until released by the last thread.