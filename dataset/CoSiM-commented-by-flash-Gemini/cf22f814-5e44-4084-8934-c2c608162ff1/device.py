"""
This module implements a distributed device system for sensor data processing,
orchestrating tasks across multiple devices. It leverages a worker pool for
parallel script execution within each device and a conditional reusable barrier
for network-wide synchronization between timepoints.

Core components:
- Device: Represents a single node in the distributed network, managing its
          sensor data, assigned scripts, and coordinating with a supervisor.
- DeviceThread: The primary thread for each Device, orchestrating fetching
                neighbors, dispatching scripts to a WorkerPool, and synchronizing
                at a global barrier.
- WorkerPool: Manages a collection of WorkerThread instances, providing a mechanism
              to submit and await completion of tasks (script executions).
- WorkerThread: A worker unit from the WorkerPool, responsible for executing a
                single script, gathering data from neighbors, running the script,
                and propagating results.
"""

from threading import Lock, Event, Thread
from barrier import ReusableBarrierCond # Custom module for reusable barrier with conditions.
from workerpool import WorkerPool # Custom module for managing a pool of worker threads.


class Device(object):
    """
    Represents an individual device in a distributed sensor network.
    Each device holds local sensor data, receives processing scripts, and
    interacts with a supervisor and other devices to exchange data and results.
    """
    def __init__(self, device_id, sensor_data, supervisor):
        """
        Initializes a Device instance.

        Args:
            device_id (int): A unique integer identifier for this device.
            sensor_data (dict): A dictionary mapping data locations (str) to their current values.
            supervisor (Supervisor): A reference to the central supervisor object,
                                     used to query for neighboring devices.
        
        Functional Utility: Sets up the device's unique state, including its ID,
                            local data, communication link to the supervisor,
                            and internal mechanisms for script management and threading.
        """
        self.device_id = device_id
        self.sensor_data = sensor_data
        self.supervisor = supervisor
        self.scripts = [] # List to store assigned scripts, each as a (script_object, location_str) tuple.
        self.timepoint_done = Event() # Event to signal when the device is ready to process scripts for a timepoint.

        # Shared synchronization primitives, initialized by the master device (device_id 0).
        self.barrier = None # Will hold a reference to the global ReusableBarrierCond instance.
        self.locks = None   # Will hold a dictionary of Locks for each data location, shared across devices.

        self.thread = DeviceThread(self) # The dedicated thread that manages this device's operations.
        self.thread.start() # Starts the DeviceThread immediately upon Device instantiation.


    def __str__(self):
        """
        Returns a human-readable string representation of the Device.
        Functional Utility: Provides a concise identifier for the device, useful in logging and debugging.
        """
        return "Device %d" % self.device_id

    def setup_devices(self, devices):
        """
        Initializes and propagates shared synchronization mechanisms across all devices in the network.
        This setup phase is coordinated by the master device (device_id 0).

        Args:
            devices (list): A list of all Device objects participating in the network.
        
        Functional Utility: Ensures that all devices share the same barrier and
                            location-specific locks for consistent and synchronized operations.
        Block Logic: Only the master device (ID 0) performs the initialization of the
                     `ReusableBarrierCond` and the `location_locks` dictionary. It then
                     assigns these shared instances to all other devices. The `location_locks`
                     are generated by iterating through all sensor data locations known
                     across all devices to ensure every unique location has a dedicated lock.
        Pre-condition: This method is called once per simulation, typically by the supervisor.
        """
        self.devices = devices
        
        if self.device_id == 0: # Master device coordinates setup.

            num_threads = len(devices)
            barrier = ReusableBarrierCond(num_threads) # Create the global reusable barrier.
            location_locks = {} # Dictionary to store Locks for each unique data location.

            # Identify all unique data locations across all devices and create a Lock for each.
            for device in devices:
                for location in device.sensor_data:
                    if location not in location_locks:
                        location_locks[location] = Lock()

            # Assign shared barrier and locks to the master device.
            if self.barrier is None:
                self.barrier = barrier
                self.locks = location_locks

            # Propagate shared barrier and locks to all other devices.
            for device in devices:
                if device.barrier is None:
                    device.barrier = barrier
                if device.locks is None:
                    device.locks = location_locks

    def assign_script(self, script, location):
        """
        Queues a script for execution at a specific data location on this device.

        Args:
            script (Script): The executable script object.
            location (str): The identifier for the data location the script will operate on.
        
        Functional Utility: Adds processing tasks to the device's internal queue.
        Block Logic: Appends the (script, location) tuple to the `scripts` list.
                     If `script` is `None`, it acts as a signal that script assignment
                     for the current timepoint is complete, and the `timepoint_done`
                     event is set to unblock the `DeviceThread`.
        """
        if script is not None:
            self.scripts.append((script, location))
        else:
            self.timepoint_done.set() # Signal that all scripts for this timepoint have been assigned.

    def get_data(self, location):
        """
        Retrieves the sensor data associated with a given location from the device's local store.
        Note: Locking for read access to `sensor_data` is handled by `WorkerThread`
              using `self.locks[location]`, ensuring data consistency during script execution.

        Args:
            location (str): The identifier for the data location.

        Returns:
            Any: The data value at the specified `location`, or `None` if the location is not found.
        Functional Utility: Provides access to the device's current sensor readings.
        """
        data = None

        if location in self.sensor_data:
            data = self.sensor_data[location]
        return data

    def set_data(self, location, data):
        """
        Updates the sensor data for a specified location in the device's local store.
        Note: Locking for write access to `sensor_data` is handled by `WorkerThread`
              using `self.locks[location]`, ensuring data consistency during script execution.

        Args:
            location (str): The identifier for the data location to update.
            data (Any): The new data value to set for the location.
        Functional Utility: Modifies the device's current sensor readings.
        """
        if location in self.sensor_data:
            self.sensor_data[location] = data

    def shutdown(self):
        """
        Initiates a graceful shutdown of the device by waiting for its
        main processing thread (`DeviceThread`) to complete.
        Functional Utility: Ensures all pending operations are finished before termination.
        """
        self.thread.join()


class DeviceThread(Thread):
    """
    The main operational thread for a Device. It orchestrates the device's
    workflow across multiple timepoints, including fetching neighboring devices,
    managing script execution through a `WorkerPool`, and synchronizing
    with other DeviceThreads via a global barrier.
    """
    def __init__(self, device):
        """
        Initializes the DeviceThread.

        Args:
            device (Device): The Device instance that this thread is responsible for.
        Functional Utility: Establishes the link between the thread and its parent Device,
                            and sets up the internal `WorkerPool` for parallel processing.
        """
        Thread.__init__(self, name="Device Thread %d" % device.device_id)
        self.device = device
        # Initializes a WorkerPool with 8 threads to execute scripts concurrently.
        self.pool = WorkerPool(8, self.device)

    def run(self):
        """
        The main execution loop for the DeviceThread.
        It continuously processes timepoints: gets neighbors, waits for script assignments,
        submits scripts to the worker pool, waits for their completion, and synchronizes
        with other devices. It breaks when no more neighbors are returned by the supervisor.
        """
        while True:
            # Block Logic: Queries the supervisor for the current set of neighboring devices.
            #              If `None` is returned, it signifies the end of the simulation.
            neighbours = self.device.supervisor.get_neighbours()
            if neighbours is None:
                # Functional Utility: Signals all worker threads in the pool to terminate.
                #                     This is achieved by submitting `None` tasks equal
                #                     to the number of worker threads.
                for _ in xrange(self.pool.max_threads):
                    self.pool.submit_work(None, None, None) # Special "STOP" signal for workers.
                self.pool.wait_completion() # Wait until all workers have processed their termination signals.
                break # Exit the main processing loop.

            # Block Logic: Waits until the `timepoint_done` event is set, indicating
            #              that all scripts for the current timepoint have been assigned.
            self.device.timepoint_done.wait()

            # Block Logic: Submits each assigned script to the `WorkerPool` for execution.
            #              The pool will dispatch these scripts to its `WorkerThread`s.
            for (script, location) in self.device.scripts:
                self.pool.submit_work(neighbours, script, location)

            # Functional Utility: Blocks until all scripts submitted to the `WorkerPool`
            #                     for the current timepoint have completed their execution.
            self.pool.wait_completion()

            # Functional Utility: Synchronizes with all other DeviceThreads in the network.
            #                     Ensures all devices have completed their processing for
            #                     the current timepoint before any proceeds to the next.
            self.device.barrier.wait()

            # Functional Utility: Resets the `timepoint_done` event for the next timepoint's processing cycle.
            self.device.timepoint_done.clear()

        # Functional Utility: Signals the `WorkerPool` to gracefully terminate its worker threads.
        self.pool.end_threads()


from threading import Thread
from Queue import Queue


class WorkerPool(object):
    """
    Manages a pool of `WorkerThread` instances, distributing tasks (script executions)
    among them. It provides methods to submit work, wait for completion, and gracefully
    terminate the worker threads.
    """
    def __init__(self, no_workers, device):
        """
        Initializes the WorkerPool with a specified number of worker threads.

        Args:
            no_workers (int): The number of `WorkerThread`s to create and manage.
            device (Device): A reference to the parent Device object.
        Functional Utility: Sets up the task queue and initializes the pool of worker threads.
        """
        self.max_threads = no_workers # Maximum number of concurrent worker threads.
        self.queue = Queue(no_workers) # A queue to hold tasks for worker threads. Its size limits pending tasks.
        self.device = device # Reference to the parent Device.
        self.thread_list = [] # List to store references to the WorkerThread instances.
        
        # Block Logic: Creates and starts each `WorkerThread`.
        for _ in range(no_workers):
            thread = WorkerThread(self.device, self.queue)
            self.thread_list.append(thread)
            thread.start() # Start the worker thread's execution loop.

    def submit_work(self, neighbours, script, location):
        """
        Submits a new task (script execution) to the worker queue.
        
        Args:
            neighbours (list): List of neighboring devices for the script to interact with.
            script (Script): The script object to be executed.
            location (str): The data location relevant to the script.
        Functional Utility: Adds a processing task to the queue for asynchronous execution by a `WorkerThread`.
        """
        self.queue.put((neighbours, script, location))

    def wait_completion(self):
        """
        Blocks until all tasks previously submitted to the queue have been processed.
        Functional Utility: Ensures that all asynchronous script executions for a timepoint are finished.
        """
        self.queue.join()

    def end_threads(self):
        """
        Gracefully terminates all `WorkerThread`s in the pool.
        Functional Utility: Ensures proper shutdown by waiting for each worker thread to finish its current task and exit.
        """
        # Block Logic: Signals each worker thread to stop by placing `None` tasks in the queue.
        #              Then, joins each thread to wait for its termination.
        for _ in self.thread_list:
            self.queue.put((None, None, None)) # A specific signal for worker threads to terminate.
        for thread in self.thread_list:
            thread.join() # Wait for each worker thread to fully stop.
        self.thread_list = [] # Clear the list of worker threads.


class WorkerThread(Thread):
    """
    A worker thread from the `WorkerPool` that continuously fetches tasks (script executions)
    from a shared queue, processes them, and handles data propagation.
    """
    def __init__(self, device, tasks):
        """
        Initializes a WorkerThread.

        Args:
            device (Device): A reference to the parent Device object.
            tasks (Queue): The shared queue from which this worker will retrieve tasks.
        Functional Utility: Sets up the worker thread with access to the device's context
                            and the shared task queue.
        """
        Thread.__init__(self)
        self.device = device
        self.tasks = tasks # The shared Queue instance.

    def run(self):
        """
        The main execution loop for a WorkerThread.
        It continuously retrieves tasks from the queue, processes them by:
        1. Acquiring a location-specific lock.
        2. Gathering data from the local device and neighbors.
        3. Executing the script.
        4. Propagating the results.
        It terminates upon receiving a special `None` task.
        """
        while True:
            # Block Logic: Retrieves a task from the shared queue. This call blocks
            #              until a task is available.
            neighbours, script, location = self.tasks.get()

            # Functional Utility: Checks for the termination signal.
            #                     If a (None, None, None) tuple is received, the thread exits.
            if neighbours is None and script is None and location is None:
                self.tasks.task_done() # Mark this termination signal as processed.
                return # Terminate the worker thread.

            # Block Logic: Acquires a location-specific lock from the device's shared locks
            #              dictionary. This ensures exclusive access to the data at 'location'
            #              across all devices during script execution.
            # Pre-condition: `self.device.locks` is initialized and contains a Lock for `location`.
            with self.device.locks[location]:
                script_data = [] # List to collect data needed by the script.
                
                # Block Logic: Iterates through neighboring devices to collect their data for the given location.
                for device in neighbours:
                    data = device.get_data(location)
                    if data is not None:
                        script_data.append(data)
                
                # Block Logic: Collects the local device's data for the given location.
                data = self.device.get_data(location)
                if data is not None:
                    script_data.append(data)

                if script_data != []: # Only run script if there is data to process.
                    # Functional Utility: Executes the script's `run` method with the collected data.
                    result = script.run(script_data)

                    # Block Logic: Propagates the result of the script execution to all neighboring devices.
                    for device in neighbours:
                        device.set_data(location, result)
                    
                    # Updates the local device's sensor data with the script's result.
                    self.device.set_data(location, result)

            # Functional Utility: Marks the current task as complete in the queue.
            #                     This allows `WorkerPool.wait_completion()` to track progress.
            self.tasks.task_done()
