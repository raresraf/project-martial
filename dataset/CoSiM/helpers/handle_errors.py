import os

def find_non_existent_directories(directory_list):
    """
    Checks a list of directories and returns those that do not exist.

    Args:
        directory_list (list): A list of strings, where each string is a
                               path to a directory.

    Returns:
        list: A new list containing paths of directories that do not exist.
    """
    non_existent_dirs = []
    for directory in directory_list:
        if not os.path.isdir(directory):
            non_existent_dirs.append(directory)
            print(f"'{directory}' does not exist.")
        else:
            print(f"'{directory}' exists.")
    return non_existent_dirs

